using Z = MInt<P>;

int cnt = 0;
map<int,Z>mem;

Z f(int n)
{   
    cnt++;
    int k = n / 2;
    if(n == 1 || n == 2)return 1;
    if(mem.count(n))return mem[n];
    if(n % 2 == 0){
        Z a = f(k + 1), b = f(k);
        return mem[n] = b*(2* a - b);
    }
    else{
        Z a = f(k + 1), b = f(k);
        return mem[n] = a * a + b * b; 
    }
}

void solve()
{
    constexpr int N = (1 << 20) - 1;
    f(N);
    cout<<log2(N)<<" "<<cnt<<endl;
    return;
}

